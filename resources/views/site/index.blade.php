        $('.slider-img-1,.slider-img-2').css('display', 'block');
        $("#slider").responsiveSlides({
            auto: true,
            pager: true,
            nav: false,
            speed: 500,
            namespace: "callbacks",
            before: function () {
                $('.events').append("<li>before event fired.</li>");
            },
            after: function () {
                $('.events').append("<li>after event fired.</li>");
            }
        });


        $('#btnSub').click(function () {
            var username = $('#username').val();
            var email = $('#email').val();
            var message = $('#message').val();

            if (!username || !email || !message) {
                alert('请填写完整的信息，谢谢您的配合');
                return false;
            } else {
                //验证
                var reg = /^1[3456789]\d{9}$/;
                if (!reg.test(email)) {
                    //手机不对，验证邮箱
                    var reg = /^([A-Za-z0-9_\-.])+@([A-Za-z0-9_\-.])+\.([A-Za-z]{2,})$/;
                    if(!reg.test(email)){
                        alert('联系方式/邮箱错误，请重新输入');
                        $("input[name='Email']").focus();
                        return false;
                    }
                }
                $.ajaxSetup({
                    headers: {'X-CSRF-TOKEN': '{{ csrf_token() }}'}
                });

                $.ajax({
                    url: "{{url('contact/suggestion')}}",
                    type: "POST",
                    data: {username: username, email: email, content: message},
                    success: function (result) {
                        console.log(result);
                        if (result.code == 200) {
                            alert('感谢您的建议，请留意邮箱邮件回复。');
                            window.location.reload();
                        }
                    }
                });

            }
        });


    });
</script>
</body>
</html>
